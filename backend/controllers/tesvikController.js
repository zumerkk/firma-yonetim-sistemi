const updateTesvik = async (req, res) => {
  try {
    const { id } = req.params;
    const updateData = req.body;

    // Validation kontrol√º
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Girilen bilgilerde hatalar var',
        errors: errors.array()
      });
    }

    const tesvik = await Tesvik.findById(id);
    if (!tesvik || !tesvik.aktif) {
      return res.status(404).json({
        success: false,
        message: 'Te≈üvik bulunamadƒ±'
      });
    }

    // Deƒüi≈üiklikleri kaydet (revizyon i√ßin)
    const eskiVeri = tesvik.toSafeJSON();
    
    // G√ºncelleme verisini uygula - null/undefined alanlarƒ± filtrele
    const filteredUpdateData = Object.fromEntries(
      Object.entries(updateData).filter(([key, value]) => {
        // firma alanƒ± null ise g√ºncelleme
        if (key === 'firma' && (value === null || value === undefined || value === '')) {
          return false;
        }
        return value !== null && value !== undefined;
      })
    );
    
    Object.assign(tesvik, filteredUpdateData);
    tesvik.sonGuncelleyen = req.user._id;
    tesvik.sonGuncellemeNotlari = updateData.guncellemeNotu || '';

    // Mali hesaplamalarƒ± otomatik g√ºncelle
    tesvik.updateMaliHesaplamalar();
    
    // Durum deƒüi≈ümi≈üse rengi g√ºncelle
    if (updateData.durumBilgileri?.genelDurum) {
      tesvik.updateDurumRengi();
    }

    await tesvik.save();

    // Deƒüi≈üen alanlarƒ± tespit et
    const changedFields = [];
    const fieldLabels = {
      'yatirimciUnvan': 'Yatƒ±rƒ±mcƒ± √únvanƒ±',
      'yatirimciAdres': 'Yatƒ±rƒ±mcƒ± Adresi',
      'yatirimciTelefon': 'Telefon',
      'yatirimciEmail': 'E-posta',
      'yatirimTutari': 'Yatƒ±rƒ±m Tutarƒ±',
      'istihdam.mevcutKisi': 'Mevcut Ki≈üi Sayƒ±sƒ±',
      'istihdam.yeniKisi': 'Yeni Ki≈üi Sayƒ±sƒ±',
      'durumBilgileri.genelDurum': 'Genel Durum',
      'durumBilgileri.durumAciklamasi': 'Durum A√ßƒ±klamasƒ±',
      'maliHesaplamalar.toplamYatirim': 'Toplam Yatƒ±rƒ±m',
      'maliHesaplamalar.tesvikTutari': 'Te≈üvik Tutarƒ±',
      'notlar.dahiliNotlar': 'Dahili Notlar',
      'notlar.resmiAciklamalar': 'Resmi A√ßƒ±klamalar'
    };

    // Nested object deƒüi≈üikliklerini kontrol et
    const checkNestedChanges = (oldObj, newObj, prefix = '') => {
      if (!oldObj || !newObj) return;
      
      Object.keys(newObj).forEach(key => {
        const fullKey = prefix ? `${prefix}.${key}` : key;
        const oldValue = oldObj[key];
        const newValue = newObj[key];
        
        if (typeof newValue === 'object' && newValue !== null && !Array.isArray(newValue)) {
          checkNestedChanges(oldValue, newValue, fullKey);
        } else if (oldValue !== newValue && fieldLabels[fullKey]) {
          changedFields.push({
            field: fieldLabels[fullKey],
            oldValue: oldValue || '-',
            newValue: newValue || '-'
          });
        }
      });
    };

    // Ana alanlarƒ± kontrol et
    Object.keys(updateData).forEach(key => {
      if (key === 'guncellemeNotu') return; // Skip update note
      
      const oldValue = eskiVeri[key];
      const newValue = updateData[key];
      
      if (typeof newValue === 'object' && newValue !== null && !Array.isArray(newValue)) {
        checkNestedChanges(oldValue, newValue, key);
      } else if (oldValue !== newValue && fieldLabels[key]) {
        changedFields.push({
          field: fieldLabels[key],
          oldValue: oldValue || '-',
          newValue: newValue || '-'
        });
      }
    });

    // Activity log
    await Activity.logActivity({
      action: 'update',
      category: 'tesvik',
      title: 'Te≈üvik G√ºncellendi',
      description: `${tesvik.tesvikId} numaralƒ± te≈üvik g√ºncellendi (${changedFields.length} alan deƒüi≈ütirildi)`,
      targetResource: {
        type: 'tesvik',
        id: tesvik._id,
        name: tesvik.yatirimciUnvan,
        tesvikId: tesvik.tesvikId
      },
      user: {
        id: req.user._id,
        name: req.user.adSoyad,
        email: req.user.email,
        role: req.user.rol
      },
      changes: {
        before: eskiVeri,
        after: tesvik.toSafeJSON(),
        fields: changedFields
      },
      metadata: {
        ip: req.ip,
        userAgent: req.get('User-Agent'),
        source: 'web'
      }
    });

    await tesvik.populate('firma', 'tamUnvan firmaId');
    await tesvik.populate('sonGuncelleyen', 'adSoyad email');

    res.json({
      success: true,
      message: 'Te≈üvik ba≈üarƒ±yla g√ºncellendi',
      data: tesvik.toSafeJSON()
    });

  } catch (error) {
    console.error('üö® Te≈üvik g√ºncelleme hatasƒ±:', error);
    res.status(500).json({
      success: false,
      message: 'Te≈üvik g√ºncellenirken hata olu≈ütu',
      error: error.message
    });
  }
};

