export const searchOSB = (query) => {
  if (!query) return osbListesi;
  
  const lowerQuery = query.toLowerCase();
  return osbListesi.filter(item => 
    item.il.toLowerCase().includes(lowerQuery) ||
    item.osb.toLowerCase().includes(lowerQuery)
  );
};

export const getOSBByIl = (il) => {
  return osbListesi.filter(item => item.il === il);
};

