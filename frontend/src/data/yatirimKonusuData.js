// 🔍 Arama ve filtreleme fonksiyonları
export const searchYatirimKonusu = (query) => {
  if (!query) return yatirimKonusuKodlari;
  
  const lowerQuery = query.toLowerCase();
  return yatirimKonusuKodlari.filter(item => 
    item.kod.toLowerCase().includes(lowerQuery) ||
    item.aciklama.toLowerCase().includes(lowerQuery) ||
    item.kategori.toLowerCase().includes(lowerQuery)
  );
};

export const getByKategori = (kategori) => {
  return yatirimKonusuKodlari.filter(item => item.kategori === kategori);
};

export const getByKod = (kod) => {
  return yatirimKonusuKodlari.find(item => item.kod === kod);
};

