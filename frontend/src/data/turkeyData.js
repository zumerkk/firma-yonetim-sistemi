// ðŸ” Ä°l arama fonksiyonu
export const searchCities = (searchTerm) => {
  if (!searchTerm) return TURKEY_CITIES;
  
  const term = searchTerm.toUpperCase().trim();
  return TURKEY_CITIES.filter(city => 
    city.includes(term)
  );
};

// ðŸ” Ä°lÃ§e arama fonksiyonu
export const searchDistricts = (city, searchTerm = '') => {
  if (!city || !CITY_DISTRICTS[city.toUpperCase()]) return [];
  
  const districts = CITY_DISTRICTS[city.toUpperCase()];
  
  if (!searchTerm) return districts;
  
  const term = searchTerm.toUpperCase().trim();
  return districts.filter(district => 
    district.includes(term)
  );
};

// ðŸŽ¯ Ä°l doÄŸrulama
export const isValidCity = (city) => {
  if (!city) return false;
  return TURKEY_CITIES.includes(city.toUpperCase());
};

// ðŸŽ¯ Ä°lÃ§e doÄŸrulama
export const isValidDistrict = (city, district) => {
  if (!city || !district) return false;
  
  const cityUpper = city.toUpperCase();
  const districtUpper = district.toUpperCase();
  
  const districts = CITY_DISTRICTS[cityUpper];
  return districts && districts.includes(districtUpper);
};

// ðŸ“Š Ä°statistikler
export const getTurkeyStats = () => {
  const totalCities = TURKEY_CITIES.length;
  const totalDistricts = Object.values(CITY_DISTRICTS)
    .reduce((sum, districts) => sum + districts.length, 0);
  
  return {
    totalCities,
    totalDistricts,
    averageDistrictsPerCity: Math.round(totalDistricts / totalCities)
  };
};

