// 🔍 Arama ve filtreleme fonksiyonları
export const searchOncelikliYatirim = (query) => {
  if (!query) return oncelikliYatirimTurleri;
  
  const lowerQuery = query.toLowerCase();
  return oncelikliYatirimTurleri.filter(item => 
    item.baslik.toLowerCase().includes(lowerQuery) ||
    item.aciklama.toLowerCase().includes(lowerQuery) ||
    item.kategori.toLowerCase().includes(lowerQuery) ||
    item.kod.toLowerCase().includes(lowerQuery)
  );
};

export const getByKategori = (kategori) => {
  return oncelikliYatirimTurleri.filter(item => item.kategori === kategori);
};

export const getById = (id) => {
  return oncelikliYatirimTurleri.find(item => item.id === id);
};

